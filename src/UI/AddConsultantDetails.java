    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        ConsultantDB consultant = new ConsultantDB();
        
        consultant.setName(txtName.getText().toString());
        if (txtName.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your name!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        consultant.setEmail(txtEmail.getText().toString());
        if (txtEmail.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your Email!", "Error", JOptionPane.ERROR_MESSAGE);
        return; 
        }
        
        try {
            consultant.setContactNumber(txtContactNumber.getText().toString());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,"Invalid Contact Number! Please enter digits only.","Error", JOptionPane.ERROR_MESSAGE);
        }
        if (txtContactNumber.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your contact number!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        String category= listCategory.getSelectedItem().toString();
        if (category.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select Consultant Category", "Error",JOptionPane.ERROR_MESSAGE);
        }
        consultant.setCategory(category);
        
        new DBConnection(consultant);
         ManagerDashboard back = new ManagerDashboard();
        back.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

