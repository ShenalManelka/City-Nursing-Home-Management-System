    private void btnCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAccountActionPerformed
        CityNursingDB obj = new CityNursingDB();
       
       obj.setName(txtName.getText().toString());
       if (txtName.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your name!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
       
       obj.setEmail(txtEmail.getText().toString());
       if (txtEmail.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your email!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
       
        try {
            obj.setContactNumber(txtContactNumber.getText().toString());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,"Invalid Contact Number! Please enter digits only.","Error", JOptionPane.ERROR_MESSAGE);
        }
        if (txtContactNumber.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your contact number!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        String password = txtPassword.getText();
        if (txtPassword.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your password!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        String conPassword = txtConfirmPassword.getText();
        if (txtConfirmPassword.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please confirm your password!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        if (!password.equals(conPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
       obj.setPassword(txtPassword.getText().toString());
       obj.setConPassword(txtConfirmPassword.getText().toString());
       
       String role = getSelectedRole();
        if (role.isEmpty()) {
          JOptionPane.showMessageDialog(this,"Please select a role !","Error",JOptionPane.ERROR_MESSAGE);  
        }
        
       obj.setRole(role);
       
       new DBConnection(obj);
       

       ManagerDashboard signin = new ManagerDashboard();
       signin.setVisible(true);
       
       this.dispose();
    }//GEN-LAST:event_btnCreateAccountActionPerformed

