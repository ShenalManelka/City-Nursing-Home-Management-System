        private String getSelectedRole(){
         if (rbtnManager.isSelected()){
            return "Manager";
        }else if(rbtnCashier.isSelected()){
            return "Cashier";
        }else if (rbtnStaff.isSelected()){
            return "Staff";
        }
        return "";
        }
