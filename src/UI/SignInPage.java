       private String checkUserCredentials(String email, String password) {
    String[] files = {"Manager.txt", "Cashier.txt", "CityNursingDB.txt"}; 

    for (String fileName : files) {
        try (BufferedReader reader = new BufferedReader(new FileReader(fileName))) {
            String line;
            String storedEmail = "";
            String storedPassword = "";
            String storedRole = "";

            while ((line = reader.readLine()) != null) {
                if (line.startsWith("Email:")) {
                    storedEmail = line.substring(6).trim();
                } else if (line.startsWith("Password:")) {
                    storedPassword = line.substring(9).trim();
                } else if (line.startsWith("Role:")) {  
                    storedRole = line.substring(5).trim();
                }

                
                if (!storedEmail.isEmpty() && !storedPassword.isEmpty() && !storedRole.isEmpty()) {
                    if (storedEmail.equals(email) && storedPassword.equals(password)) {
                        return storedRole;  
                    }
                    // Reset for the next record
                    storedEmail = "";
                    storedPassword = "";
                    storedRole = "";
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error reading file: " + fileName, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    return null;
}
