    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
    String email = txtEmail.getText();
    char[] passwordChars = txtPassword.getPassword();
    String password = new String(passwordChars);

    // Validate if email or password is empty
    if (email.isEmpty() || password.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Email and Password cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
    return;
    }

    String role = checkUserCredentials(email, password);

        if (role != null) {
            switch (role) {
                case "Manager":
                    ManagerDashboard managerDashboard = new ManagerDashboard();
                    managerDashboard.setVisible(true);
                    break;
                case "Cashier":
                    CashierDashboard cashierDashboard = new CashierDashboard();
                    cashierDashboard.setVisible(true);
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Unknown role: " + role, "Error", JOptionPane.ERROR_MESSAGE);
                    return;
            }
            this.dispose(); 
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Email or Password", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

